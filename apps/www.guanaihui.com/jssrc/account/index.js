function IndexController() {
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
     继承于Controller基类
     -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    Controller.call(this);
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    页面顶部全部服务分类下拉
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    this.servicesCategory();
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    左侧分类鼠标事件
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    this.categoryListener();
};

/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
左侧分类鼠标事件
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
IndexController.prototype.categoryListener = function(){
    if($(".category li").size() <= 1) return;
    var _this = this;
    $(".category li").mouseover(function(){
        var $_this = $(this);
        $_this.addClass("on").siblings().removeClass("on");
    })
}
/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
 类的初始化
 -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
$(document).ready(function() {
    new IndexController;
});
