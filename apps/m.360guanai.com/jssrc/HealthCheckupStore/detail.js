function DetailController() {
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    继承于Controller基类
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    Controller.call(this);
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    banner的swiper滑动
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    this.swipeBanner();
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    tab内容切换
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    this.swapTabs();

    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    iframe 自适应高度
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    this.reinitFrame();
};

/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
banner的swiper滑动
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
DetailController.prototype.swipeBanner = function() {
    require([this.utilStaticPrefix + "/swiper/dist/js/swiper.min.js"], function() {
        var swiper = new Swiper("#banner", {
            pagination: ".swiper-pagination",
            paginationClickable: true,
            spaceBetween: 30,
            centeredSlides: true,
            autoplay: 5000,
            autoplayDisableOnInteraction: false
        });
    });
};

/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
tab内容切换
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
DetailController.prototype.swapTabs = function() {
    require([this.utilStaticPrefix + "/jquery.tabs.min.js"], function() {
        $(".tabs").tabs({
            "eventType": "click",
            "effect": "slideDown",
            "duration": 200
        });
    });
};

/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
iframe 自适应高度
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
DetailController.prototype.reinitFrame = function() {
   
}


/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
类的初始化
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
$(document).ready(function() {
    new DetailController;
});