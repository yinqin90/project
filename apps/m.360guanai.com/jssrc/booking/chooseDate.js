function ChooseDateController() {
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    继承于Controller基类
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    Controller.call(this);
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    选择日期
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    this.dateSelected();
};
/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
选择日期
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
ChooseDateController.prototype.dateSelected = function() {
    $("table td").click(function() {
        var _this = $(this);
        if (_this.hasClass("on")) {
            $("table td").removeClass("selected");
            _this.addClass("selected");
        }
    });
};

/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
类的实例化
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
$(document).ready(function() {
    new ChooseDateController;
});
