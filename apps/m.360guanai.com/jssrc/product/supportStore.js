function SupportStore(params) {
    this.scope = params.scope;
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    初始化支持门店tooltip
     -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    this.initToolTip((params === null || params.trigger === null || params.trigger === undefined) ? "click" : params.trigger);
};
/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
初始化支持门店tooltip
 -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
SupportStore.prototype.initToolTip = function(trigger) {
    $(this.scope).find('[data-toggle="tooltip"]').tooltip({
        'trigger': trigger
    });
    $(this.scope).find('[data-toggle="tooltip"]').click(function() {
        var _this = $(this);
        _this.siblings('[data-toggle="tooltip"]').tooltip('hide');
    });
};
