function Ua() {
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    以下为静态属性
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    this.nav = window.navigator;
    this.navua = this.nav.userAgent.toLowerCase();
}

Ua.prototype = {
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    获取ua
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    ua: function() {
        return this.navua;
    },
    //苹果google内核,chrome
    isWebkit: function() {
        return this.navua.indexOf('applewebkit') > -1;
    },
    //获取chrome版本
    getChromeVersion: function() {
        var Sys = {},
            s;
        (s = this.navua.match(/chrome\/([\d.]+)/)) ? Sys.chrome = s[1]: 0;
        return Sys.chrome;
    },
    //火狐内核
    isGecko: function() {
        return this.navua.indexOf('gecko') > -1 && this.navua.indexOf('khtml') == -1 && this.navua.indexOf('trident') == -1;
    },
    //获取火狐版本
    getFFVersion: function() {
        var Sys = {},
            s;
        (s = this.navua.match(/firefox\/([\d.]+)/)) ? Sys.firefox = s[1]: 0;
        return Sys.firefox;
    },
    //IE内核
    isTrident: function() {
        return this.navua.indexOf('trident') > -1;
    },
    //获取IE版本
    getIeVersion: function() {
        var Sys = {},
            s;
        (s = this.navua.match(/rv:([\d.]+)\) like gecko/)) ? Sys.ie = s[1]:
            (s = this.navua.match(/msie ([\d.]+)/)) ? Sys.ie = s[1] : 0;
        return Sys.ie;
    },
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    判断是否为移动终端
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    isMobile: function() {
        return !!this.navua.match(/applewebkit.*mobile.*/);
    },
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    判断是否为android终端或者uc浏览器
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    isAndroid: function() {
        return this.navua.indexOf('android') > -1 || this.navua.indexOf('linux') > -1;
    },
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    判断是否为iOS终端
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    isIOS: function() {
        return !!this.navua.match(/\(i[^;]+;( u;)? cpu.+mac os x/);
    },

    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    判断是否为iphone
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    isIphone: function() {
        return this.navua.indexOf('iphone') > -1;
    },
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    判断是否为iPad
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    isIPad: function() {
        return this.navua.indexOf('ipad') > -1;
    },
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    是否web应用程序，没有头部与底部
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    isWebApp: function() {
        return this.navua.indexOf('safari') == -1;
    },
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    是否微信浏览器
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    isWeixin: function() {
        return this.navua.indexOf('micromessenger') > -1;
    },
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    是否qq浏览器
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    isQQ: function() {
        return this.navua.match(/\sqq/i) == "qq";
    }
};
